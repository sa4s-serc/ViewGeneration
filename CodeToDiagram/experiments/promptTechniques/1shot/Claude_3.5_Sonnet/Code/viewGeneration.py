import os
import subprocess
import json
import shutil
import tempfile
import glob
import anthropic

# Initialize Claude client
client = anthropic.Anthropic(api_key="")  # Replace with actual key or use os.getenv

def load_one_shot_example(behavior, json_path="example_prompts.json"):
    try:
        with open(json_path, 'r', encoding='utf-8') as f:
            examples = json.load(f)
            for ex in examples:
                if ex['behavior'] == behavior:
                    return [ex]
            return [examples[0]]
    except Exception as e:
        print(f"Error loading one-shot example: {e}")
        return None

def get_plantuml_from_summary(summary, repo_name, concern, behavior, error_message=None, code=None):
    example_data = load_one_shot_example(behavior)
    example = example_data[0] if example_data else None
    if not example:
        return "Error: Failed to load one-shot example."

    # Set diagram details
    if behavior == "static":
        diagram_type = "component diagram"
        diagram_goal = "- Clearly shows the main components and their relationships"
    else:
        diagram_type = "sequence diagram"
        diagram_goal = "- Captures runtime interactions between components"

    # Build system instruction
    task_instruction = f"""
You are an expert software architect. Your task is to design a view of a system using **PlantUML diagrams**. 
Based on the summary, concern, and behavior, generate a **PlantUML {diagram_type}** that:
{diagram_goal}
- Matches the system **behavior**
- Addresses the architectural **concern**
- Uses valid PlantUML syntax
- Returns only PlantUML code (no explanation)
This is the repository name, so name the diagram/image: {repo_name}.
"""

    if error_message:
        task_instruction += f"\nNote: A previous attempt failed with this error:\n{error_message}\nProblematic code:\n{code}\nPlease regenerate the diagram correctly."

    # Add one-shot example
    task_instruction += f"""

### Example Input:
Summary: {example['summary']}
Concern: {example['concern']}
Behavior: {example['behavior']}

### Example Output:
{example['plantuml_code']}
"""

    # Construct Claude-compatible call
    try:
        message = client.messages.create(
            model="claude-3-5-sonnet-20240620",
            max_tokens=1024,
            temperature=0.7,
            system=task_instruction,
            messages=[
                {
                    "role": "user",
                    "content": [
                        {
                            "type": "text",
                            "text": f"""### Actual Input:
Summary: {summary}
Concern: {concern}
Behavior: {behavior}

Please generate the appropriate PlantUML diagram."""
                        }
                    ]
                }
            ]
        )
        # Return only the text blocks
        return ''.join(part.text for part in message.content if part.type == "text")
    except Exception as e:
        return f"Error: {e}"




def save_plantuml_code(puml_code, repo_name):
    os.makedirs("oneShot_claude_plantumlcode", exist_ok=True)
    # clean_repo_name = repo_name.replace('/', '_').replace('\\', '_').rstrip('_')
    file_path = os.path.join("oneShot_claude_plantumlcode", f"{repo_name}.puml")
    with open(file_path, "w", encoding="utf-8") as file:
        file.write(puml_code)
    return file_path



def compile_plantuml(repo_name, input_path, output_dir="../oneShot_claude_output_images"):
    os.makedirs(output_dir, exist_ok=True)

    with tempfile.TemporaryDirectory() as temp_dir:
        # Run PlantUML in temporary directory
        result = subprocess.run(
            ["plantuml", "-o", temp_dir, input_path],
            capture_output=True, text=True
        )

        if result.returncode != 0:
            return False, result.stderr

        # Find the generated file (e.g., .png)
        generated_files = glob.glob(os.path.join(temp_dir, "*"))
        if not generated_files:
            return False, "No file generated by PlantUML."

        # Assume one diagram per file — take the first
        generated_file = generated_files[0]
        ext = os.path.splitext(generated_file)[1]
        output_path = os.path.join(output_dir, repo_name + ext)

        shutil.move(generated_file, output_path)

        return True, None



def process_view(repo_name, summary, concern, behavior):

    # Generate and compile PlantUML code with retry mechanism
    max_retries = 3
    attempt = 0
    cnt=0
    
    # Open log file in append mode
    with open("error.log", "a", encoding="utf-8") as log_file:
        while attempt < max_retries:
            puml_code = get_plantuml_from_summary(summary, repo_name, concern, behavior,
                                                error_message=None if attempt == 0 else error_message, code=None if attempt == 0 else puml_code)
            file_path = save_plantuml_code(puml_code, repo_name)
            success, error_message = compile_plantuml(repo_name,file_path)
            
            if success:
                log_file.write(f"Successfully processed repo {repo_name}\n")
                log_file.write(f"PlantUML diagram generated at {file_path}\n")
                break
            else:
                log_file.write(f"Attempt {attempt + 1}: Error in generating PlantUML for {repo_name}\n")
                log_file.write(f"Error message: {error_message}\n")
                attempt += 1
                
        if attempt == max_retries:
            cnt+=1
            log_file.write(f"Failed to generate valid PlantUML for {repo_name} after {max_retries} attempts\n")

    return cnt 


def main():
    input_jsonl = "../../../../Architectural_knowledge_extraction/generated_summaries.jsonl"
    column_name1 = "Repository Name"
    column_name2 = "summary"
    column_name3 = "Concern"
    column_name4 = "Behavior"
    output_dir = "oneShot_claude_output_images"
    globcnt=0

    try:
        with open(input_jsonl, 'r', encoding='utf-8') as f:
            entries = [json.loads(line) for line in f]
    except Exception as e:
        print(f"Error reading JSONL file: {e}")
        return

    for entry in entries:
        if all(key in entry for key in [column_name1, column_name2, column_name3, column_name4]):
            clean_repo_name = entry[column_name1].replace('/', '_').replace('\\', '_').rstrip('_')
            expected_output_path = os.path.join(output_dir, f"{clean_repo_name}.png")

            if os.path.exists(expected_output_path):
                print(f"Skipping {clean_repo_name} — already exists.")
                continue

            globcnt+=process_view(clean_repo_name, entry[column_name2], entry[column_name3], entry[column_name4])
            print(f"Processed entry: {clean_repo_name}")
        else:
            print(f"Skipping entry due to missing required fields: {entry}")

    print("All processing complete. Results saved in results folder.")
    print(f"✅ Results saved in {output_dir}. Total failed attempts: {globcnt}")


if __name__ == "__main__":
    main()
