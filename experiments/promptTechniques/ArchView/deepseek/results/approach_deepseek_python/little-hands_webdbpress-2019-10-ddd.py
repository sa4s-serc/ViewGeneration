import graphviz

dot = graphviz.Digraph(comment='Domain-Driven Design Sample Application Architecture')
dot.attr(rankdir='TB', splines='ortho')

with dot.subgraph(name='cluster_infrastructure') as infra:
    infra.attr(label='Infrastructure Layer', style='filled', color='lightgrey')
    infra.node('dao', 'ScreeningDao\nInterviewDao', shape='box')
    infra.node('jdbc_repo', 'ScreeningJdbcRepository', shape='box')
    infra.node('repo', 'ScreeningRepository', shape='box')

with dot.subgraph(name='cluster_application') as app:
    app.attr(label='Application Layer', style='filled', color='lightblue')
    app.node('app_v1', 'ScreeningApplicationServiceV1', shape='box')
    app.node('app_v2', 'ScreeningApplicationServiceV2', shape='box')
    app.node('app_v3', 'ScreeningApplicationServiceV3', shape='box')
    app.node('app_v4', 'ScreeningApplicationServiceV4', shape='box')

with dot.subgraph(name='cluster_domain') as domain:
    domain.attr(label='Domain Layer', style='filled', color='lightgreen')
    domain.node('screening_v1', 'ScreeningV1', shape='box')
    domain.node('screening_v2', 'ScreeningV2', shape='box')
    domain.node('screening_v3', 'ScreeningV3', shape='box')
    domain.node('screening_v4', 'ScreeningV4', shape='box')
    domain.node('interview_v1', 'InterviewV1', shape='box')
    domain.node('interview_v2', 'InterviewV2', shape='box')
    domain.node('interview_v3', 'InterviewV3', shape='box')
    domain.node('interview_v4', 'InterviewV4', shape='box')
    domain.node('email', 'EmailAddress', shape='box')
    domain.node('screening_id', 'ScreeningId', shape='box')
    domain.node('interviews', 'Interviews', shape='box')
    domain.node('status_v1', 'ScreeningStatusV1', shape='box')
    domain.node('status_v2', 'ScreeningStatusV2', shape='box')
    domain.node('status_v3', 'ScreeningStatusV3', shape='box')
    domain.node('status_v4', 'ScreeningStatusV4', shape='box')
    domain.node('step_result', 'ScreeningStepResult', shape='box')

with dot.subgraph(name='cluster_shared') as shared:
    shared.attr(label='Shared Components', style='filled', color='yellow')
    shared.node('app_exception', 'ApplicationException', shape='box')
    shared.node('const', 'Const', shape='box')

dot.node('main', 'DddSampleApplication\n(Spring Boot Entry Point)', shape='ellipse')
dot.node('tests', 'DddSampleApplicationTests', shape='box')

dot.edge('app_v1', 'screening_v1')
dot.edge('app_v2', 'screening_v2')
dot.edge('app_v3', 'screening_v3')
dot.edge('app_v4', 'screening_v4')
dot.edge('screening_v2', 'interview_v2')
dot.edge('screening_v3', 'interview_v3')
dot.edge('screening_v4', 'interview_v4')
dot.edge('screening_v3', 'email')
dot.edge('screening_v3', 'screening_id')
dot.edge('screening_v4', 'interviews')
dot.edge('screening_v1', 'status_v1')
dot.edge('screening_v2', 'status_v2')
dot.edge('screening_v3', 'status_v3')
dot.edge('screening_v4', 'status_v4')
dot.edge('screening_v4', 'step_result')
dot.edge('app_v1', 'dao')
dot.edge('app_v2', 'dao')
dot.edge('app_v3', 'dao')
dot.edge('app_v4', 'dao')
dot.edge('repo', 'jdbc_repo')
dot.edge('app_v3', 'repo')
dot.edge('app_v4', 'repo')
dot.edge('main', 'app_v1')
dot.edge('main', 'app_v2')
dot.edge('main', 'app_v3')
dot.edge('main', 'app_v4')
dot.edge('tests', 'main')

dot.render('ddd_architecture_diagram', format='png', cleanup=True)